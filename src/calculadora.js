let calculadora = {
    sumar:function(primerNumero,segundoNumero){

        return primerNumero+segundoNumero      
    },
    Resta:function(primerNumero,segundoNumero){
        return primerNumero-segundoNumero        
    },
    Multiplicar:function(primerNumero,segundoNumero){
        return primerNumero*segundoNumero        
    },
    Dividir:function(primerNumero,segundoNumero){
        return primerNumero/segundoNumero        
    }
    
}


export {calculadora}